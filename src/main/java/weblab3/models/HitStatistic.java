package weblab3.models;

public class HitStatistic {

    private int hitCount;
    private int missHitCound;

    public int getHitCount() {
        return hitCount;
    }

    public void setHitCount(int hitCount) {
        this.hitCount = hitCount;
    }

    public int getMissHitCount() {
        return missHitCound;
    }

    public void setMissHitCount(int missHitCound) {
        this.missHitCound = missHitCound;
    }
}
